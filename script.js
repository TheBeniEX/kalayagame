document.addEventListener("DOMContentLoaded", () => {
  const navToggle = document.querySelector("[data-nav-toggle]");
  const nav = document.querySelector("[data-nav]");
  const navLinks = document.querySelectorAll(".site-nav a");
  const langButtons = document.querySelectorAll("[data-lang-button]");
  const backToTop = document.querySelector("[data-back-to-top]");
  const audio = document.getElementById("site-music");
  const musicButton = document.getElementById("music-toggle");
  const musicLabel = musicButton ? musicButton.querySelector(".music-label") : null;
  const musicSection = document.querySelector(".music-section");
  const musicCard = document.querySelector(".music-card");
  const lightbox = document.querySelector("[data-lightbox-modal]");
  const lightboxImage = document.querySelector("[data-lightbox-image]");
  const lightboxCaption = document.querySelector("[data-lightbox-caption]");
  const lightboxClose = document.querySelector("[data-lightbox-close]");
  const galleryItems = document.querySelectorAll("[data-lightbox]");
  let currentLang = getStoredLang();

  applyLanguage(currentLang);

  if (navToggle && nav) {
    navToggle.addEventListener("click", () => {
      const isOpen = nav.classList.toggle("is-open");
      navToggle.setAttribute("aria-expanded", String(isOpen));
    });

    navLinks.forEach((link) => {
      link.addEventListener("click", () => {
        nav.classList.remove("is-open");
        navToggle.setAttribute("aria-expanded", "false");
      });
    });
  }

  langButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const nextLang = button.getAttribute("data-lang-button");

      if (!nextLang || !translations[nextLang]) {
        return;
      }

      currentLang = nextLang;
      storeLang(currentLang);
      applyLanguage(currentLang);
      updateMusicState(audio ? !audio.paused : false);
    });
  });

  document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
    anchor.addEventListener("click", (event) => {
      const targetId = anchor.getAttribute("href");

      if (!targetId || targetId === "#") {
        event.preventDefault();
        return;
      }

      const target = document.querySelector(targetId);

      if (!target) {
        return;
      }

      event.preventDefault();
      target.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  });

  if (backToTop) {
    const toggleBackToTop = () => {
      backToTop.classList.toggle("is-visible", window.scrollY > 700);
    };

    toggleBackToTop();
    window.addEventListener("scroll", toggleBackToTop, { passive: true });
    backToTop.addEventListener("click", () => {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  }

  if (audio && musicButton) {
    const source = audio.querySelector("source[src]");

    if (!source) {
      hideMusicSection();
    } else {
      audio.addEventListener("error", hideMusicSection);
      audio.addEventListener("pause", () => updateMusicState(false));
      audio.addEventListener("play", () => updateMusicState(true));

      musicButton.addEventListener("click", async () => {
        if (audio.paused) {
          try {
            await audio.play();
          } catch {
            hideMusicSection();
          }
          return;
        }

        audio.pause();
      });
    }
  } else {
    hideMusicSection();
  }

  const closeLightbox = () => {
    if (!lightbox || !lightboxImage) {
      return;
    }

    lightbox.classList.remove("is-open");
    lightbox.setAttribute("aria-hidden", "true");
    lightboxImage.setAttribute("src", "");
    lightboxImage.setAttribute("alt", "");
  };

  if (lightbox && lightboxImage && lightboxCaption) {
    galleryItems.forEach((item) => {
      item.addEventListener("click", () => {
        const imageSrc = item.getAttribute("data-lightbox");
        const imageTitle = getGalleryText(item, "title");
        const imageAlt = getGalleryText(item, "alt") || imageTitle;

        if (!imageSrc) {
          return;
        }

        lightboxImage.setAttribute("src", imageSrc);
        lightboxImage.setAttribute("alt", imageAlt);
        lightboxCaption.textContent = imageTitle;
        lightbox.classList.add("is-open");
        lightbox.setAttribute("aria-hidden", "false");

        if (lightboxClose) {
          lightboxClose.focus();
        }
      });
    });

    if (lightboxClose) {
      lightboxClose.addEventListener("click", closeLightbox);
    }

    lightbox.addEventListener("click", (event) => {
      if (event.target === lightbox) {
        closeLightbox();
      }
    });

    document.addEventListener("keydown", (event) => {
      if (event.key === "Escape" && lightbox.classList.contains("is-open")) {
        closeLightbox();
      }
    });
  }

  document.querySelectorAll("img").forEach((image) => {
    image.addEventListener("error", () => {
      image.hidden = true;
    });
  });

  function applyLanguage(lang) {
    const dictionary = translations[lang] || translations.en;

    document.documentElement.lang = lang === "pt" ? "pt-BR" : "en";
    document.title = dictionary.meta.title;
    setMetaContent("description", dictionary.meta.description);
    setMetaProperty("og:description", dictionary.meta.description);

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const value = getTranslation(dictionary, element.getAttribute("data-i18n"));

      if (value) {
        element.textContent = value;
      }
    });

    document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
      const value = getTranslation(dictionary, element.getAttribute("data-i18n-aria"));

      if (value) {
        element.setAttribute("aria-label", value);
      }
    });

    langButtons.forEach((button) => {
      const isActive = button.getAttribute("data-lang-button") === lang;
      button.classList.toggle("is-active", isActive);
      button.setAttribute("aria-pressed", String(isActive));
    });

    galleryItems.forEach((item) => {
      const image = item.querySelector("img");
      const translatedAlt = getGalleryText(item, "alt");

      if (image && translatedAlt) {
        image.alt = translatedAlt;
      }
    });

    if (lightbox && lightbox.classList.contains("is-open") && lightboxCaption) {
      const openItem = Array.from(galleryItems).find((item) => item.getAttribute("data-lightbox") === lightboxImage.getAttribute("src"));

      if (openItem) {
        lightboxCaption.textContent = getGalleryText(openItem, "title");
        lightboxImage.alt = getGalleryText(openItem, "alt") || lightboxCaption.textContent;
      }
    }
  }

  function updateMusicState(isPlaying) {
    const dictionary = translations[currentLang] || translations.en;

    if (musicButton) {
      musicButton.classList.toggle("is-playing", isPlaying);
      musicButton.setAttribute("aria-label", isPlaying ? dictionary.music.pause : dictionary.music.play);
    }

    if (musicCard) {
      musicCard.classList.toggle("is-playing", isPlaying);
    }

    if (musicLabel) {
      musicLabel.textContent = isPlaying ? dictionary.music.pause : dictionary.music.play;
    }
  }

  function hideMusicSection() {
    if (musicSection) {
      musicSection.hidden = true;
    }
  }

  function getGalleryText(item, kind) {
    return item.getAttribute(`data-${kind}-${currentLang}`) || item.getAttribute(`data-${kind}-en`) || "";
  }
});

function getStoredLang() {
  try {
    const storedLang = window.localStorage.getItem("kalaya-language");
    return translations[storedLang] ? storedLang : "en";
  } catch {
    return "en";
  }
}

function storeLang(lang) {
  try {
    window.localStorage.setItem("kalaya-language", lang);
  } catch {
    // Local storage can be unavailable in strict privacy contexts.
  }
}

function getTranslation(dictionary, path) {
  if (!path) {
    return "";
  }

  return path.split(".").reduce((value, key) => {
    if (value && Object.prototype.hasOwnProperty.call(value, key)) {
      return value[key];
    }

    return "";
  }, dictionary);
}

function setMetaContent(name, content) {
  const element = document.querySelector(`meta[name="${name}"]`);

  if (element) {
    element.setAttribute("content", content);
  }
}

function setMetaProperty(property, content) {
  const element = document.querySelector(`meta[property="${property}"]`);

  if (element) {
    element.setAttribute("content", content);
  }
}
